const CONTRACT_ADDRESS = "0x5FbDB2315678afecb367f032d93F642f64180aa3";

const Home = () => {
    const { connect, connectors, error, isConnecting, pendingConnector } = useConnect();
    const { data: account } = useAccount();
    const { activeChain, switchNetwork } = useNetwork({
        chainId: chain.localhost.id,
    });
    const [mintPrice, setMintPrice] = useState(0)
    const { disconnect } = useDisconnect();
    const { data: balanceOfMine, isBalanceOfMineError, isBalanceOfMineLoading } = useContractRead(
        {
            addressOrName: CONTRACT_ADDRESS,
            contractInterface: CONTRACT_ABI,
        },
        'balanceOfMine',
        { watch: true },
    )

    const { data: maxSupply } = useContractRead(
        {
            addressOrName: CONTRACT_ADDRESS,
            contractInterface: CONTRACT_ABI,
        },
        'maxSupply',
        { watch: true },
    )

    const { data: saleStartTime } = useContractRead(
        {
            addressOrName: CONTRACT_ADDRESS,
            contractInterface: CONTRACT_ABI,
        },
        'saleStartTime',
        { watch: true },
    )

    const { data: availableSupply } = useContractRead(
        {
            addressOrName: CONTRACT_ADDRESS,
            contractInterface: CONTRACT_ABI,
        },
        'getAvailableSupply',
        { watch: true },
    )
    const { data: mintData, isError: mintError, isLoading: isMintLoading, write: mint } = useContractWrite(
        {
            addressOrName: CONTRACT_ADDRESS,
            contractInterface: CONTRACT_ABI,
        },
        'mint',
        {
            overrides: {
                from: account?.address,
                value: ethers.utils.parseEther(String(mintPrice.length ? mintPrice : 0)),
            },
        },
    )

    const mintNft = () => { mint() }

    useEffect(
        () => {
            if (activeChain && switchNetwork && activeChain.id !== chain.localhost.id) {
                switchNetwork(chain.localhost.id)
            }
        }, [activeChain, switchNetwork]
    )

    return (
        <div className="App">
            <header className="App-header">
                {
                    account ?
                        (
                            <div>
                                <div>錢包地址: {account.address}</div>
                                {account.connector && <div>連結方式: {account.connector.name}</div>}
                                <button onClick={disconnect}>取消連結錢包</button>
                                <h1> NFT 數據 </h1>
                                {maxSupply && <p> NFT 庫存量: {maxSupply.toString()} </p>}
                                {balanceOfMine && <p> 我的 NFT 數量: {balanceOfMine.toString()} </p>}
                                {availableSupply && <p> 剩餘NFT 數量: {availableSupply.toString()} </p>}
                                {saleStartTime && <p> 拍賣開始: {moment.unix(saleStartTime / 1000).fromNow()} </p>}
                            </div>
                        ) :
                        (
                            <div>
                                {
                                    connectors.map(
                                        (connector) => (
                                            <button
                                                disabled={!connector.ready}
                                                key={connector.id}
                                                onClick={() => connect(connector)}
                                            >
                                                {connector.name}
                                                {!connector.ready && " (不支援)"}
                                                {isConnecting && connector.id === pendingConnector?.id && " (連結中)"}
                                            </button>
                                        )
                                    )
                                }
                            </div>
                        )
                }
                {!isMintLoading && activeChain &&
                    <>
                        <input type="number" placeholder='Mint price' onChange={e => setMintPrice(e.target.value)}></input>
                        <label>ETH</label>
                        <div>

                            <button onClick={mintNft}>mint</button>
                        </div>
                    </>
                }
            </header>
        </div>
    );
}


export default Home